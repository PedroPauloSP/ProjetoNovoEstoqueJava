    private void preenchercmbCliente() {
        ClienteDao clienteDao = new ClienteDao();

        List<Cliente> listaCliente = clienteDao.getCliente("");
        for (Cliente C : listaCliente) {
            cmbCliente.addItem(C);
        }

    }

    private void preenchercmbEquipamento() {
        EquipamentoDao equipamentoDao = new EquipamentoDao();
        List<Equipamento> listaequip = equipamentoDao.getEquipamento("");
        for (Equipamento E : listaequip) {
            cmbequipamentoid.addItem(E);
        }
    }

    private void preenchercmbCategoria() {
        CategoriaDao categoriaDao = new CategoriaDao();
        List<Categoria> listacat = categoriaDao.getCategoria("");
        for (Categoria C : listacat) {
            cmbcategoriaid.addItem(C);
        }
    }

