    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        int quantidade = Integer.parseInt(txtQuantidade.getText());
        String data = txtData.getText();

        Cliente cliente = new Cliente();
        Equipamento equipamentos = new Equipamento();
        Saidas saida = new Saidas();
        Categoria categorias = new Categoria();
        Equipamento equipamentoid = (Equipamento) cmbequipamentoid.getSelectedItem();
        Categoria categoriaid = (Categoria) cmbcategoriaid.getSelectedItem();
        Cliente Clienteid = (Cliente) cmbCliente.getSelectedItem();

        saida.setEquipamentoid(equipamentoid);
        saida.setCategoriaid(categoriaid);
        saida.setQuantidade(quantidade);
        saida.setClienteid(Clienteid);

        SimpleDateFormat conversor = new SimpleDateFormat("dd/MM/yyyy");

        try {
            Date dataConvertida = conversor.parse(data);
            java.sql.Date sqlDate = new java.sql.Date(dataConvertida.getTime());
            saida.setData(sqlDate);
        } catch (ParseException pe) {
            System.out.println("Erro na conversão da data " + pe.getMessage());
        }

        SaidasDao saidaDao = new SaidasDao();
        saidaDao.Cadastrar(saida);

        txtQuantidade.setText("");
        txtData.setText("");

    }//GEN-LAST:event_btnCadastrarActionPerformed

