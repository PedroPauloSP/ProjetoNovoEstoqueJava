    private void Preenchertabela() {

        SaidasDao saidaDao = new SaidasDao();

        List<Saidas> listaSaida = saidaDao.getSaidas();

        DefaultTableModel tabelasaida = (DefaultTableModel) tblSaida.getModel();
        tblSaida.setRowSorter(new TableRowSorter(tabelasaida));
        tabelasaida.setNumRows(0);

        for (Saidas S : listaSaida) {
            Object[] obj = new Object[]{
                S.getId(),
                S.getClienteid().getRazaoSocial(),
                S.getEquipamentoid().getNome(),
                S.getCategoriaid().getNomeCategoria(),
                S.getData(),
                S.getQuantidade()
            };
            tabelasaida.addRow(obj);

        }

    }

