    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        String data = txtData.getText();
        int quantidade = Integer.parseInt(txtQuantidade.getText());

        Equipamento equip = new Equipamento();
        Entradas entrada = new Entradas();
        Categoria cat = new Categoria();

        Equipamento equipamentoid = (Equipamento) cmbEquipamento.getSelectedItem();
        Fornecedor fornecedorid = (Fornecedor) cmbFornecedor.getSelectedItem();
        Categoria categoriaid = (Categoria) cmbCategoriaid.getSelectedItem();

        entrada.setFornecedorid(fornecedorid);
        entrada.setEquipamentoid(equipamentoid);
        entrada.setCategoriaid(categoriaid);
        entrada.setQuantidade(quantidade);
        
        SimpleDateFormat conversor = new SimpleDateFormat("dd/MM/yyyy");

        try {
            Date dataConvertida = conversor.parse(data);
            java.sql.Date sqlDate = new java.sql.Date(dataConvertida.getTime());
            entrada.setData(sqlDate);
        } catch (ParseException pe) {
            System.out.println("Erro na convers√£o da data " + pe.getMessage());
        }

        EntradasDao entradaDao = new EntradasDao();
        entradaDao.Cadastrar(entrada);

        txtData.setText("");
        txtQuantidade.setText("");
    }//GEN-LAST:event_btnCadastrarActionPerformed

