    private void preencherTabela() {

        EntradasDao entradaDao = new EntradasDao();

        List<Entradas> listaEntrada = entradaDao.getEntradas();

        DefaultTableModel tabelaentrada = (DefaultTableModel) tblentrada.getModel();
        tblentrada.setRowSorter(new TableRowSorter(tabelaentrada));
        tabelaentrada.setNumRows(0);

        for (Entradas E : listaEntrada) {
            Object[] obj = new Object[]{
                E.getId(),
                E.getFornecedorid().getRazaoSocial(),
                E.getEquipamentoid().getNome(),
                E.getCategoriaid().getNomeCategoria(),
                E.getData(),
                E.getQuantidade()
            };
            tabelaentrada.addRow(obj);

        }

    }

