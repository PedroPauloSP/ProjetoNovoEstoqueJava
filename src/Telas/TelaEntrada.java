    private void preenchercmbFornecedor() {
        FornecedorDao fornecedorDao = new FornecedorDao();

        List<Fornecedor> listaFornecedor = fornecedorDao.getFornecedor("");
        for (Fornecedor F : listaFornecedor) {
            cmbFornecedor.addItem(F);
        }

    }

    private void preenchercmbEquipamento() {
        EquipamentoDao equipamentoDao = new EquipamentoDao();
        List<Equipamento> listaEquipamento = equipamentoDao.getEquipamento("");
        for (Equipamento E : listaEquipamento) {
            cmbEquipamento.addItem(E);
        }

    }

    private void preenchercmbCategoriaid() {
        CategoriaDao categoriaDao = new CategoriaDao();
        List<Categoria> listaCategoria = categoriaDao.getCategoria("");
        for (Categoria C : listaCategoria) {
            cmbCategoriaid.addItem(C);
        }
    }

